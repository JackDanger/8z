// ── Deflate64 cross-validation (encode + decode via gzippy 0.8) ──────────────

/// Encode bytes with 7zippy's Deflate64Coder, decode them back, assert equality.
///
/// This is a pure round-trip within 7zippy (no `7zz` required). Uses a varied
/// fixture: zeros, sequential bytes, and ASCII mixed together.
#[cfg(feature = "deflate64")]
#[test]
fn deflate64_round_trip() {
    use crate::pipeline::deflate64::Deflate64Coder;
    use crate::pipeline::Coder;

    // Varied fixture: zeros, sequential, ASCII.
    let mut payload: Vec<u8> = Vec::with_capacity(768);
    payload.extend(std::iter::repeat_n(0u8, 256));
    payload.extend(0u8..=255u8);
    payload.extend(
        b"Hello, from 7zippy Deflate64 round-trip test!"
            .iter()
            .cycle()
            .take(256),
    );

    let coder = Deflate64Coder;
    let compressed = coder.encode(&payload).expect("Deflate64 encode failed");
    let decompressed = coder
        .decode(&compressed, payload.len() as u64)
        .expect("Deflate64 decode failed");

    assert_eq!(
        decompressed, payload,
        "Deflate64 round-trip mismatch: encode then decode did not reproduce the original"
    );
}

