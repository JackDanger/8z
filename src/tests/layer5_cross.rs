/// Attempting to extract an encrypted archive without a password returns an error.
#[cfg(feature = "aes")]
#[test]
fn extract_without_password_returns_encrypted_error() {
    let bytes =
        std::fs::read(fixture_path("aes_lzma2.7z")).expect("aes_lzma2.7z must be committed");
    let archive = Archive::parse(&bytes).expect("must parse aes_lzma2.7z");
    let result = archive.reader().extract(0);
    assert!(
        matches!(result, Err(crate::SevenZippyError::EncryptedContent(_))),
        "expected EncryptedContent error, got: {result:?}"
    );
}

/// Read the committed PPMd fixture (generated by `7zz`) and extract it with 7zippy.
#[cfg(feature = "ppmd")]
#[test]
fn we_read_committed_ppmd_fixture() {
    let bytes =
        std::fs::read(fixture_path("ppmd_default.7z")).expect("ppmd_default.7z must be committed");
    let archive = Archive::parse(&bytes).expect("must parse ppmd_default.7z");
    assert_eq!(archive.file_count(), 1);
    let content = archive.reader().extract(0).unwrap();
    // Payload is bytes(range(256)) * 4 = 1024 bytes
    let expected: Vec<u8> = (0u8..=255).cycle().take(1024).collect();
    assert_slices_eq!(content, expected);
}
