/// Ask `7zz` to compress data with the Delta filter (standalone), then decompress
/// with 7zippy.
#[cfg(feature = "delta")]
#[test]
fn we_read_seven_zips_delta_archive() {
    require_7zz!();

    // Use a sequential byte pattern — highly predictable, so delta-encoding
    // collapses it to all-1s which is a good functional check.
    let input: Vec<u8> = (0u8..=255).cycle().take(1024).collect();
    let archive = seven_zip_compress(&input, &CoderSpec::Delta { distance: 1 });

    let parsed = Archive::parse(&archive).expect("7zippy must parse 7zz Delta archive");
    assert_eq!(parsed.file_count(), 1);
    let extracted = parsed.reader().extract(0).unwrap();
    assert_slices_eq!(extracted, input);
}

/// Read the committed delta fixture (generated by `7zz`) and extract it with 7zippy.
#[cfg(feature = "delta")]
#[test]
fn we_read_committed_delta_fixture() {
    let bytes = std::fs::read(fixture_path("delta_distance1.7z"))
        .expect("delta_distance1.7z must be committed");
    let archive = Archive::parse(&bytes).expect("must parse delta_distance1.7z");
    assert_eq!(archive.file_count(), 1);
    let content = archive.reader().extract(0).unwrap();
    // Payload is bytes(range(256)) * 4 = 1024 bytes
    let expected: Vec<u8> = (0u8..=255).cycle().take(1024).collect();
    assert_slices_eq!(content, expected);
}

