//! Layer 5: cross-validation tests — 7zippy reads what 7zz writes; 7zz reads what 7zippy writes.
//!
//! Every test in this module requires the `7zz` CLI. Tests are skipped
//! automatically (via `require_7zz!()`) on machines where it is not installed.
//! CI is configured to always install `7zz`, so the skip never hides a real
//! failure in CI.
//!
//! Live tests cover the Copy coder. Every other codec row is `#[ignore]`d
//! until that codec is implemented.

use super::fixtures;
use super::oracle::require_7zz;
use super::oracle::{seven_zip_compress, seven_zip_decompress, CoderSpec};
use crate::{assert_slices_eq, Archive, ArchiveBuilder};

fn fixture_path(name: &str) -> std::path::PathBuf {
    let manifest = std::path::Path::new(env!("CARGO_MANIFEST_DIR"));
    manifest.join("corpora/fixtures/archives").join(name)
}

// ── 7zz reads what 7zippy writes ─────────────────────────────────────────────

/// Write a Copy-coder archive with 7zippy, then extract it with `7zz`.
///
/// Migrated and generalised from `oracle_copy::seven_zip_can_extract_our_copy_archive`.
#[test]
fn seven_zip_reads_our_copy_archive() {
    require_7zz!();

    let payload = b"Hello, from 7zippy's own writer!".to_vec();
    let mut b = ArchiveBuilder::new();
    b.add_copy_file("greeting.txt", payload.clone());
    let archive_bytes = b.build().unwrap();

    // seven_zip_decompress writes the archive to a tempdir and runs 7zz x.
    let extracted = seven_zip_decompress(&archive_bytes);
    assert_slices_eq!(extracted, payload);
}

/// Write a 64 KiB Copy archive with 7zippy, extract with `7zz`.
#[test]
fn seven_zip_reads_our_copy_64k_archive() {
    require_7zz!();

    let input = fixtures::random(0x1234_5678, 65_536);
    let mut b = ArchiveBuilder::new();
    b.add_copy_file("payload.bin", input.clone());
    let archive_bytes = b.build().unwrap();

    let extracted = seven_zip_decompress(&archive_bytes);
    assert_slices_eq!(extracted, input);
}

// ── 7zippy reads what 7zz writes ─────────────────────────────────────────────

/// Ask `7zz` to compress some bytes with the Copy coder, then read the
/// resulting archive with 7zippy.
#[test]
fn we_read_seven_zips_copy_archive() {
    require_7zz!();

    let input = fixtures::random(0xABCD_EF01, 1024);
    let archive = seven_zip_compress(&input, &CoderSpec::Copy);

    let parsed = Archive::parse(&archive).expect("7zippy must parse 7zz Copy archive");
    assert_eq!(parsed.file_count(), 1);
    let extracted = parsed.reader().extract(0).unwrap();
    assert_slices_eq!(extracted, input);
}

/// Read the committed `copy_only_64k.7z` fixture (generated by `7zz`) and
/// extract it with 7zippy.
#[test]
fn we_read_committed_copy_only_64k_fixture() {
    let bytes = std::fs::read(fixture_path("copy_only_64k.7z"))
        .expect("copy_only_64k.7z must be committed");
    let archive = Archive::parse(&bytes).expect("must parse copy_only_64k.7z");
    assert_eq!(archive.file_count(), 1);
    assert_eq!(archive.file_name(0), Some("random_64k.bin"));
    let content = archive.reader().extract(0).unwrap();
    assert_eq!(content.len(), 65_536);
}

